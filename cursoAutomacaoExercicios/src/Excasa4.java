public class Excasa4 {
    //criar um método sem retorno que receba com parâmetro uma palavra e imprima a palavra 5 vezes

    public static void imprimirPalavra(String palavra){
        int i =0;
        while(i<5){
            System.out.println(palavra);
            i++;
        }
    }
    public static void main(String[] args){
        imprimirPalavra("Sabado");
    }


}
    
    
       

     